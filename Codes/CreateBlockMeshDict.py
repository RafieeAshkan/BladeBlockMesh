# ==============================================================================
# Creates blockMeshDict file from these 4 inputs:
#     verts, hex, edges, bound
# ==============================================================================
import os
# ==============================================================================
class BlockMesh:
    def CreateBlockMeshDict():
        # ==============================================================================
        # Writing blockMeshDict
        # ==============================================================================
        if os.path.exists('../Output/blockMeshDict'):
            os.remove('../Output/blockMeshDict')
        output = open(os.path.abspath('../Output/blockMeshDict'), "a")
        # ==============================================================================
        # Header
        # ==============================================================================
        output.write( \
            "// * * * * * * * * * * * * * * * * * * * " + \
            "* * * * * * * * * * * * * * * * * * //"
            "\nFoamFile" + \
            "\n{" + \
            "\n\tversion     2.0;" + \
            "\n\tformat      ascii;" + \
            "\n\tclass       dictionary;" + \
            "\n\tobject      blockMeshDict;" + \
            "\n}" + \
            "\n// * * * * * * * * * * * * * * * * * * * " + \
            "* * * * * * * * * * * * * * * * * * //")
        # ==============================================================================
        # Vertices
        # ==============================================================================
        output.write("\nconvertToMeters 1;\n")
        output.write("\nvertices\n(")

        for line in BlockMesh.verts:
            output.write(f"{line}")

        output.write("\n);")
        # ==============================================================================
        # Blocks
        # ==============================================================================
        output.write("\n\nblocks\n(")

        for line in BlockMesh.hex:
            output.write(f"{line}")

        output.write("\n);")
        # ==============================================================================
        # Edges
        # ==============================================================================
        output.write("\n\nedges\n(")

        for line in BlockMesh.edges:
            output.write(f"{line}")

        output.write("\n);")
        # ==============================================================================
        # Boundary
        # ==============================================================================
        output.write("\n\nboundary\n(")

        for line in BlockMesh.bound:
            output.write(f"{line}")

        output.write("\n);")
        # ==============================================================================
        # MergePatch
        # ==============================================================================
        #        output.write("\nmergePatchPairs\n(")
        #
        #        output.write("\n\t(merge hub)")
        #
        #        output.write("\n);")
        # ==============================================================================
        output.write("\n// *********************" + \
                     "****************************************" + \
                     "************ //")
        output.close()
# ==============================================================================
